//Prepare Method @135-734F574D
    function Prepare()
    {
        global $CCSLocales;
        global $DefaultDateFormat;
        $this->wp = new clsSQLParameters($this->ErrorBlock);
        $this->wp->AddParameter("1", "urlINPUT_DATA_CONTROL_ID", ccsFloat, "", "", $this->Parameters["urlINPUT_DATA_CONTROL_ID"], 0, false);
        $this->AllParametersSet = $this->wp->AllParamsSet();
    }
//End Prepare Method

//Open Method @135-4E70154D
    function Open()
    {
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeBuildSelect", $this->Parent);
        $this->SQL = "SELECT   a.*,\n" .
        "         b.CODE,\n" .
        "         c.FINANCE_PERIOD_CODE\n" .
        "  FROM   INPUT_DATA_CONTROL a, P_REFERENCE_LIST b, P_FINANCE_PERIOD c\n" .
        "  WHERE  a.INPUT_DATA_CLASS_ID = b.P_REFERENCE_LIST_ID\n" .
        "  AND    a.P_FINANCE_PERIOD_ID = c.P_FINANCE_PERIOD_ID\n" .
        "  AND    a.INPUT_DATA_CONTROL_ID = " . $this->SQLValue($this->wp->GetDBValue("1"), ccsFloat) . "";
        $this->Order = "";
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeExecuteSelect", $this->Parent);
        $this->query(CCBuildSQL($this->SQL, $this->Where, $this->Order));
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "AfterExecuteSelect", $this->Parent);
    }
//End Open Method

//SetValues Method @135-64EACEA9
